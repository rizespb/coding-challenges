const getDpMax = (dp, flag) => {
  let dpMax = 0;

  for (let index = 0; index < dp.length; index++) {
    if (dp[index] === dp[index - 1]) continue;

    let left = index;

    while (left > 0 && dp[left - 1] >= dp[index]) {
      left--;
    }
    let right = index;

    while (right < dp.length - 1 && dp[right + 1] >= dp[index]) {
      right++;
    }

    const length = right - left + 1;

    dpMax = Math.max(dpMax, Math.min(length, dp[index]));
  }

  return dpMax * dpMax;
};

const maximalSquare = (matrix) => {
  const dp = [];

  let maxSquare = 0;

  for (let i = 0; i < matrix.length; i++) {
    for (let k = 0; k < matrix[0].length; k++) {
      dp[k] = matrix[i][k] === '0' ? 0 : (dp[k] ?? 0) + 1;
    }

    maxSquare = Math.max(getDpMax(dp, i === 2), maxSquare);
  }

  return maxSquare;
};

