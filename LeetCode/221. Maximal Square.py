class Solution:
    def maximalSquare(self, matrix: list[list[str]]) -> int:
        dp = [1 if value == "1" else 0 for value in matrix[0]]

        max_square = self.getRowMaxSquare(dp)

        for row in matrix[1:]:
            for index, value in enumerate(row):
                dp[index] = dp[index] + 1 if value == "1" else 0

            max_square = max(max_square, self.getRowMaxSquare(dp))

        return max_square

    def getRowMaxSquare(self, dp: list[int]) -> int:
        max_dp_square = 0

        for index, current in enumerate(dp):
            if index != 0 and current == dp[index - 1]:
                continue

            left = index

            while left > 0 and dp[left - 1] >= current:
                left -= 1

            right = index

            while right < len(dp) - 1 and dp[right + 1] >= current:
                right += 1

            length = right - left + 1

            max_dp_square = max(max_dp_square, min(current, length))

        return max_dp_square * max_dp_square


